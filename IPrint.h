#pragma once

#include <iostream>
#include <string>

/**
 * @brief Простір імен University для компонентів управління університетом
 */
namespace University {

    /**
     * @class IPrint
     * @brief Інтерфейс для об'єктів, що підтримують вивід інформації
     *
     * Визначає стандартний інтерфейс для всіх класів, які потребують
     * функціоналу виводу інформації на екран та перетворення в рядкове представлення.
     * Наслідування від цього інтерфейсу гарантує наявність методів print() та toString().
     */
    class IPrint
    {
    public:
        /**
         * @brief Виводить інформацію про об'єкт на екран
         *
         * Чисто віртуальний метод, який повинен бути реалізований у похідних класах.
         * Виводить відформатовану інформацію про стан об'єкта в консоль.
         */
        virtual void print() const = 0;

        /**
         * @brief Перетворює об'єкт в рядкове представлення
         * @return Рядкове представлення об'єкта
         *
         * Чисто віртуальний метод, який повинен бути реалізований у похідних класах.
         * Повертає рядок, що містить основну інформацію про об'єкт.
         */
        virtual std::string toString() const = 0;

        /**
         * @brief Віртуальний деструктор за замовчуванням
         *
         * Забезпечує коректне видалення об'єктів похідних класів
         * через покажчик на базовий клас.
         */
        virtual ~IPrint() = default;
    };

} // namespace University